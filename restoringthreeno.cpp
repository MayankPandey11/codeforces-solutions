#include <bits/stdc++.h>
using namespace std;
int main()
{
    int x1, x2, x3, x4;
    cin >> x1 >> x2 >> x3 >> x4;

    //  a+b = x1 , a+c = x2 , b+c = x3 and a+b+c = x4
    int a, b, c;

    while (x1 > a)
    {
        if (a + b == x1 || a + c = x2 || a + b + c = x3)
        {
            cout << a << endl;
        }
    }
}